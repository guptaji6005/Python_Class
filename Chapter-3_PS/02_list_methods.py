friends = ["Apple", "Orange", 5, 345.06, False, "Akash", "Rohan"]
print(friends)

friends.append("Grapes")                        # add in last of the list
print(friends)

friends.reverse()                # reverse of the list
print(friends)

friends.insert(3,9)           # insert is add 9 at 3 index
print(friends)

friends.pop(2)                 # will delete the element at the index 2 and return it value
print(friends)

friends.remove("Orange")
print(friends)

